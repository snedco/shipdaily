export async function scaffoldApp(appDir, spec) {
  // Top-level files
  await writeFile(join(appDir, 'package.json'), packageJson(spec), 'utf-8');
  await writeFile(join(appDir, 'next.config.js'), nextConfig(), 'utf-8');
  await writeFile(join(appDir, 'tsconfig.json'), tsconfig(), 'utf-8');
  await writeFile(join(appDir, 'tailwind.config.js'), tailwindConfig(), 'utf-8');
  await writeFile(join(appDir, 'postcss.config.js'), postcssConfig(), 'utf-8');
  await writeFile(join(appDir, '.env.example'), envExample(), 'utf-8');
  await writeFile(join(appDir, '.gitignore'), gitignore(), 'utf-8');
  await writeFile(join(appDir, 'README.md'), readme(spec), 'utf-8');
  await writeFile(join(appDir, 'schema.sql'), generateSchemaSQL(spec), 'utf-8');

  // app/ dir
  await mkdir(join(appDir, 'app'), { recursive: true });
  await writeFile(join(appDir, 'app', 'globals.css'), globalsCss(), 'utf-8');
  await writeFile(join(appDir, 'app', 'layout.tsx'), rootLayout(spec), 'utf-8');
  await writeFile(join(appDir, 'app', 'page.tsx'), landingPage(spec), 'utf-8');

  // app/app/page.tsx (main feature)
  await mkdir(join(appDir, 'app', 'app'), { recursive: true });
  await writeFile(join(appDir, 'app', 'app', 'page.tsx'), appPage(spec), 'utf-8');

  // lib/
  await mkdir(join(appDir, 'lib'), { recursive: true });
  await writeFile(join(appDir, 'lib', 'supabase-browser.ts'), supabaseClient(), 'utf-8');
  await writeFile(join(appDir, 'lib', 'supabase-server.ts'), supabaseServer(), 'utf-8');

  return appDir;
}

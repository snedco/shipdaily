import { mkdir, writeFile } from 'node:fs/promises';
import { join } from 'node:path';

function pgType(t) {
  const map = {
    uuid: 'uuid',
    text: 'text',
    int: 'integer',
    integer: 'integer',
    bigint: 'bigint',
    bool: 'boolean',
    boolean: 'boolean',
    timestamptz: 'timestamptz',
    jsonb: 'jsonb',
    numeric: 'numeric',
  };
  return map[t.toLowerCase()] ?? t;
}

function generateSchemaSQL(spec) {
  const out = [];
  out.push('-- Generated by shipdaily for ' + spec.name);
  out.push('-- Run this in the Supabase SQL editor, or via supabase db push.');
  out.push('');
  for (const t of spec.tables) {
    const cols = t.columns.map(c => {
      const parts = [`  ${c.name} ${pgType(c.type)}`];
      if (c.primary) parts.push('primary key default gen_random_uuid()');
      if (c.references) parts.push(`references ${c.references}(id) on delete cascade`);
      if (c.default) parts.push(`default ${c.default}`);
      if (c.notNull) parts.push('not null');
      return parts.join(' ');
    }).join(',\n');
    out.push(`create table if not exists public.${t.name} (`);
    out.push(cols);
    out.push(');');
    out.push('');
    out.push(`alter table public.${t.name} enable row level security;`);
    out.push('');
    out.push(`create policy "Users manage own rows" on public.${t.name}`);
    out.push(`  for all using (auth.uid() = user_id) with check (auth.uid() = user_id);`);
    out.push('');
  }
  return out.join('\n');
}

